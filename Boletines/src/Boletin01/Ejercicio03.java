public class Ejercicio03 {

	public static void main(String[] args) {
		
		System.out.println("Ingles"+"\t"+"Spain");
		System.out.println("------"+"\t"+"-----");
		
		System.out.println("cat" + "\t" + "gato");
		System.out.println("lemon" + "\t" + "limon");
		System.out.println("soap" + "\t" + "jabon");
		System.out.println("chair" + "\t" + "silla");
		System.out.println("table" + "\t" + "mesa");
		System.out.println("dog" + "\t" + "perro");
		System.out.println("boat" + "\t" + "barco");
		System.out.println("mine" + "\t" + "mina");
		System.out.println("mobile" + "\t" + "movil");
		System.out.println("number" + "\t" + "numero");
		
	}
}

